package com.jda.app.domain;

import java.util.Date;

public class Venda {
	
	private Integer id;
	private Date instante;
	
	
	
	public Integer getId() {
		return id;
	}
	public void setId(Integer id) {
		this.id = id;
	}
	public Date getInstante() {
		return instante;
	}
	public void setInstante(Date instante) {
		this.instante = instante;
	}
	
	

}
